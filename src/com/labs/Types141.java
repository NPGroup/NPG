package com.labs;

public class Types141 {
    public static void main(String[] args) {
        byte bd = 127;
        short sd = 32767;
        int id = 2147483647;
        long ld = 9223372036854775807L;
        float fd = 14.141414F;
        double dd = 14.141414;
        char cd = 'd';
        boolean blt = true;
        boolean blf = false;
        System.out.println("Обьявлена и инициализирована переменная типа byte ( byte bd = 127 ): " + bd);
        System.out.println("Обьявлена и инициализирована переменная типа short ( short sd = 32767 ): " + sd);
        System.out.println("Обьявлена и инициализирована переменная типа int ( int id = 2147483647 ): " + id);
        System.out.println("Обьявлена и инициализирована переменная типа long ( long ld = 9223372036854775807L ): " + ld);
        System.out.println("Обьявлена и инициализирована переменная типа float ( float fd = 14.141414F ): " + fd);
        System.out.println("Обьявлена и инициализирована переменная типа double ( double dd = 14.141414 ): " + dd);
        System.out.println("Обьявлена и инициализирована переменная типа char ( char cd = 'd' ): " + cd);
        System.out.println("Обьявлена и инициализирована переменная типа boolean ( boolean blt = true ): " + blt);
        System.out.println("Обьявлена и инициализирована переменная типа boolean ( boolean blf = false ): " + blf);
        System.out.println("\"Код компилируется и выполняется. Все ОК!\"");
    }
}
